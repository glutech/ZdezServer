package cn.com.zdez.po;

import java.io.Serializable;
import java.sql.Time;

public class SchoolMsg implements Serializable{
	private int id;
	private String title;
	private String content;
	private String date;
	private String schoolAdminUsername;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String tile) {
		this.title = tile;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getSchoolAdminUsername() {
		return schoolAdminUsername;
	}
	public void setSchoolAdminUsername(String schoolAdminUsername) {
		this.schoolAdminUsername = schoolAdminUsername;
	}

}
